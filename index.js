let currentLang = navigator.language.startsWith("pt") ? "pt" : "en";

function renderPortfolio() {
  const content = currentLang === "pt" ? contentPT : contentEN;

  document.querySelector(".subtitle").textContent = content.subtitulo;

  document.querySelector("#trajetoria h2").textContent = content.trajetoriaTitulo;
  const timelineEl = document.querySelector("#trajetoria .timeline");
  timelineEl.innerHTML = "";
  content.experiencias.forEach(exp => {
    timelineEl.innerHTML += `
      <div class="timeline-item">
        <h3>${exp.empresa}</h3>
        <p class="cargo">${exp.cargo}</p>
        <p class="descricao">${exp.descricao}</p>
      </div>`;
  });

  document.querySelector("#formacao h2").textContent = content.formacaoTitulo;
  const formacaoEl = document.querySelector("#formacao .timeline");
  formacaoEl.innerHTML = "";
  content.formacoes.forEach(form => {
    formacaoEl.innerHTML += `
      <div class="timeline-item">
        <h3>${form.instituicao}</h3>
        <p class="cargo">${form.curso}</p>
        <p class="descricao">${form.descricao}</p>
      </div>`;
  });

  document.querySelector("#tecnologias h2").textContent = content.tecnologiasTitulo;
  const techList = document.querySelector(".tech-list");
  techList.innerHTML = "";
  content.tecnologias.forEach(tech => {
    techList.innerHTML += `<li>${tech}</li>`;
  });

  document.querySelector("#contato h2").textContent = content.contatoTitulo;
  const contatoP = document.querySelector("#contato p");
  contatoP.innerHTML = content.contatoLinks.map(link =>
    `<a href="${link.url}" target="_blank">${link.label}</a>`).join(" | ");
}

document.addEventListener("DOMContentLoaded", () => {
  // Efeito de digitação
  const nameTyping = document.getElementById("name-typing");
  const nameText = "Jorge Lisboa";
  let i = 0;
  function typeWriter() {
    if (i < nameText.length) {
      nameTyping.innerHTML += nameText.charAt(i);
      i++;
      setTimeout(typeWriter, 150);
    }
  }
  typeWriter();

  // Render inicial
  renderPortfolio();

  // Botão de linguagem
  document.getElementById("lang-toggle-btn").addEventListener("click", () => {
    currentLang = currentLang === "pt" ? "en" : "pt";
    renderPortfolio();
  });
}); 
